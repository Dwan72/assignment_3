package timeClass;

public class State {
    private char c;
    private int sum;
    private int total;





    public void setC(char c) {
        this.c = c;
    }
    public char getC() {
        return this.c;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }
    public int getSum() {
        return this.sum;
    }

    public void setTotal(int total) {
        this.total = total;
    }
    public int getTotal() {
        return this.total;
    }



}
